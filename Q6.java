public class Q6 {
    public static void main(String[] args) {
        //        java program to swap two numbers
        int first =34 , second = 23;
        System.out.println("printing a" + first);
        System.out.println("printing b" + second);

        int temporary = first;
        first = second;
        second = temporary;
        System.out.println("first" +  first);
        System.out.println("second" +  second);
    }
    
}
